export function getStaticPostById(id) {
  const numId = parseInt(id, 10);
  return staticBlogPosts.find((p) => p.id === numId) ?? null;
}

export function getStaticRelatedPosts(postId, limit = 3) {
  const post = getStaticPostById(postId);
  if (!post) return [];
  const others = staticBlogPosts.filter((p) => p.id !== post.id);
  const sameCategory = others.filter((p) => p.category === post.category);
  const rest = others.filter((p) => p.category !== post.category);
  return [...sameCategory, ...rest].slice(0, limit);
}
